#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	a;

	a = c;
	while (*s)
	{
		if (*s == a)
			return ((char *)s);
		if (*s == '\0')
			return (NULL);
		s++;
	}
	if (!a && *s == '\0')
		return ((char *)s);
	return (NULL);
}
